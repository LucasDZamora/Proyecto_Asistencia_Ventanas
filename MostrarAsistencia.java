    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String fecha = textoFecha.getText();
        double cotaInferior = Double.parseDouble(textoCotaInferior.getText());
        double cotaSuperior = Double.parseDouble(textoCotaSuperior.getText());

        ArrayList<Cursos> cursosEnRango = asistenciaColegio.buscarCursosPorPorcentaje(fecha, cotaInferior, cotaSuperior);

        DefaultTableModel model = (DefaultTableModel) tablaAlumnos.getModel();
        model.setRowCount(0);

        for (Cursos curso : cursosEnRango) {
            double porcentaje = curso.calcularPorcentajeAsistencia(fecha);
            model.addRow(new Object[]{curso.getNombre(), porcentaje + "%", (100 - porcentaje) + "%"});
        }
    }//GEN-LAST:event_jButton1MouseClicked

